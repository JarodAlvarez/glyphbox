#!/usr/bin/env python3
"""
qr-decode.py — decodes a GLYPHBOX Aztec card set back to a .gbcart file.

Supports three modes:
  • Static image — reads a PNG/JPG scan of a card rear
  • PDF          — renders a card PDF rear page and decodes from it
  • Webcam       — opens the system camera and scans live frames

Multi-card tiered format (generated by qr-encode.py):
  Each physical card has ONE Aztec code on its rear.
  Payload per code:
    byte[0]  — chunk index   (0 … N-1)
    byte[1]  — total cards   (N — number of cards in the set)
    byte[2]  — flags         (0x01 = zlib-compressed whole cart)
    byte[3+] — chunk data

  Card sets: 1, 2, 4, 8, or 16 cards (smallest power of 2 that fits).
  Codes are self-identifying — scan them in any order, one at a time.

  halves dict layout:
    halves[idx]  = (flags, chunk_data)  for each captured card index
    halves[-1]   = total                sentinel: total cards in set

Usage:
    python tools/qr-decode.py scan.png     -o recovered.gbcart
    python tools/qr-decode.py card.pdf     -o recovered.gbcart
    python tools/qr-decode.py --webcam     -o recovered.gbcart
    python tools/qr-decode.py --webcam --device 1 -o recovered.gbcart
"""

import argparse
import os
import sys
from pathlib import Path
import struct

def _card_label(idx: int, total: int) -> str:
    """Return a human-readable card label, e.g. '3/8'."""
    return f"{idx + 1}/{total}"

# Minimum render DPI for reliable Aztec decoding (used for PDF inputs).
# Dense Aztec codes (> ~400 bytes/chunk) need ≥7 px/module to decode; at 72 dpi
# base that requires 12×.  Lower values work for sparse carts (bouncer etc.)
# but fail silently for denser ones such as the tennis or runner carts.
_PDF_ZOOM = 12  # 12× = ~864 DPI equivalent at 72 DPI base


# ── Dependency helpers ────────────────────────────────────────────────────────

def _require(pkg_import: str, pip_name: str):
    """Import a module, exiting with a pip-install hint if missing."""
    import importlib
    try:
        return importlib.import_module(pkg_import)
    except ImportError:
        sys.exit(f"Missing dependency: pip install {pip_name}")


# ── Core Aztec scanner (uses zxing-cpp) ──────────────────────────────────────

def _aztec_hints(zxingcpp):
    """
    Build read_barcodes keyword arguments that restrict the scan to Aztec
    format and maximise the search effort for distorted/wide-angle frames.

    Restricting to Aztec makes each pass 5–10× faster on Pi ARM CPUs since
    the decoder skips QR, DataMatrix, DataBar, etc.

    try_rotate / try_downscale / try_invert are already True by default in
    zxing-cpp; we set them explicitly so older builds that default them False
    still benefit.  Note: some builds spell this "try_harder" — we probe the
    API once at startup and cache whichever kwargs are accepted.
    """
    hints: dict = {}

    # Restrict to Aztec format only
    try:
        hints["formats"] = zxingcpp.BarcodeFormat.Aztec
    except AttributeError:
        pass   # very old build without per-format filtering

    # Probe for exhaustive-search parameters — API varies by build version
    import inspect
    try:
        sig = inspect.signature(zxingcpp.read_barcodes)
        params = sig.parameters
        for key in ("try_rotate", "try_downscale", "try_invert"):
            if key in params:
                hints[key] = True
    except (TypeError, ValueError):
        pass   # can't introspect — skip

    return hints


def _play_ding() -> None:
    """
    Play a short two-tone ascending chime when a card is successfully scanned.
    Runs in a daemon thread so it never blocks the scan loop.
    Pure stdlib — no extra dependencies (wave + struct + math + subprocess).
    Falls back silently if aplay is unavailable (Mac / no ALSA).
    """
    import io, math, struct, subprocess, threading, wave

    def _build_wav(freqs_dur: list, rate: int = 44100) -> bytes:
        buf = io.BytesIO()
        with wave.open(buf, 'wb') as w:
            w.setnchannels(1)
            w.setsampwidth(2)
            w.setframerate(rate)
            samples = []
            for freq, dur, vol in freqs_dur:
                n = int(rate * dur)
                for i in range(n):
                    t        = i / rate
                    envelope = math.exp(-t * 10)          # fast exponential decay
                    s        = vol * envelope * math.sin(2 * math.pi * freq * t)
                    samples.append(struct.pack('<h', int(32767 * s)))
            w.writeframes(b''.join(samples))
        return buf.getvalue()

    def _worker():
        try:
            # Two ascending tones: 660 Hz (80 ms) then 880 Hz (160 ms)
            wav = _build_wav([(660, 0.08, 0.55), (880, 0.16, 0.70)])
            import sys as _sys, tempfile as _tmp, os as _os
            if _sys.platform == "darwin":
                # macOS: write to a temp file and play with afplay
                with _tmp.NamedTemporaryFile(suffix=".wav", delete=False) as f:
                    f.write(wav)
                    tmp_path = f.name
                try:
                    subprocess.run(
                        ['afplay', tmp_path],
                        capture_output=True, timeout=2
                    )
                finally:
                    _os.unlink(tmp_path)
            else:
                # Linux/Pi: pipe directly to aplay via stdin
                subprocess.run(
                    ['aplay', '-q', '-'],
                    input=wav, capture_output=True, timeout=2
                )
        except Exception:
            pass   # audio not available — silently skip

    threading.Thread(target=_worker, daemon=True).start()


def _is_complete(halves: dict) -> bool:
    """Return True when all expected chunks have been captured."""
    total = halves.get(-1, 0)
    if total == 0:
        return False
    return all(i in halves for i in range(total))


def _scan_for_halves(pil_img, camera_mode: bool = False) -> dict:
    """
    Scan a PIL Image for one or more Aztec codes in the multi-card format.
    Returns a dict:
        chunk-index (0 … N-1)  →  (flags_byte, chunk_data_bytes)
        -1                     →  total   (number of cards in the set)

    Payload format per code (generated by qr-encode.py):
        byte[0]  — chunk index  (0 … N-1)
        byte[1]  — total cards  (N)
        byte[2]  — flags        (0x01 = zlib-compressed)
        byte[3+] — chunk data

    Binarisation strategy:
      Pass 1 – zxing-cpp's built-in LocalAverage binariser.
               Works well for real-world webcam frames with uneven lighting.
      Pass 2 – Hard threshold to pure B&W (L < 128 → 0, else 255).
               PDF-rendered and screen-displayed codes are anti-aliased at
               module edges; the fringe grey pixels confuse LocalAverage.
               A global threshold restores the clean binary signal.

    camera_mode=True adds passes 3–4, suited to live camera frames:
      Pass 3 – CLAHE (local contrast normalisation) — evens out uneven
               lighting across the frame.
      Pass 4 – Adaptive threshold on CLAHE image — locally binarises each
               8×8 tile independently; handles gradients and glare.
    """
    zxingcpp = _require("zxingcpp", "zxing-cpp")
    halves: dict = {}
    hints = _aztec_hints(zxingcpp)   # Aztec-only + try_harder

    # Valid tier sizes produced by qr-encode.py (powers of 2, ≤ 16 cards)
    _VALID_TOTALS = frozenset({1, 2, 4, 8, 16})

    def _extract(pil) -> None:
        for r in zxingcpp.read_barcodes(pil, **hints):
            if not r.valid or len(r.bytes) < 4:
                continue
            # Only accept Aztec codes — other formats (DataBar, QR, etc.)
            # can produce false positives whose raw bytes corrupt the
            # [index, total, flags, data] payload parse.
            if "aztec" not in str(r.format).lower():
                continue
            idx   = r.bytes[0]   # chunk index (0 … N-1)
            total = r.bytes[1]   # total cards in set
            flags = r.bytes[2]   # 0x01 = zlib compressed
            data  = r.bytes[3:]
            # Reject impossible totals (must be a power-of-2 tier value)
            # and out-of-range indices.
            if total not in _VALID_TOTALS or idx >= total:
                continue
            # Record total as sentinel (first Aztec code wins)
            if -1 not in halves:
                halves[-1] = total
            if idx not in halves:
                halves[idx] = (flags, data)
                print(f"  Found Card {_card_label(idx, total)}: "
                      f"{len(data):,} bytes  (format={r.format})")

    # Pass 1 — native binariser
    _extract(pil_img)
    if _is_complete(halves):
        return halves

    # Pass 2 — hard-threshold fallback (fixes anti-aliased PDF/screen renders)
    bw = pil_img.convert("L").point(lambda v: 0 if v < 128 else 255, "1").convert("RGB")
    _extract(bw)
    if _is_complete(halves) or not camera_mode:
        return halves

    # Passes 3–12: camera-specific preprocessing ──────────────────────────
    try:
        import cv2
        import numpy as np
        from PIL import Image as _Image

        frame = np.array(pil_img.convert("RGB"))
        gray  = cv2.cvtColor(frame, cv2.COLOR_RGB2GRAY)
        H, W  = gray.shape

        clahe    = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
        enhanced = clahe.apply(gray)

        def _pil(g):
            return _Image.fromarray(cv2.cvtColor(g, cv2.COLOR_GRAY2RGB))

        # Pass 3 — CLAHE full frame
        _extract(_pil(enhanced))
        if _is_complete(halves):
            return halves

        # Pass 4 — adaptive threshold on CLAHE full frame
        adaptive = cv2.adaptiveThreshold(
            enhanced, 255, cv2.ADAPTIVE_THRESH_GAUSSIAN_C, cv2.THRESH_BINARY, 25, 5
        )
        _extract(_pil(adaptive))
        if _is_complete(halves):
            return halves

    except Exception:
        pass   # cv2/numpy unavailable or error — fall back to what we have

    return halves


